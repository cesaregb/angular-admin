'use strict';
(function() {

  class ProductComponent {
    products = [];

    constructor($stateParams, $state, noty, factoryGeneral, $confirm, $log, $uibModal) {
      this.$log = $log;
      this.$confirm = $confirm;
      this.factoryGeneral = factoryGeneral;
      this.$uibModal = $uibModal;
      this.noty = noty;
      this.$state = $state;
      this.route = $stateParams.route;
      this.getInfo();
    }

    getInfo() {
      var _this = this;
      this.factoryGeneral.getProducts().then(function(response) {
        _this.products = response;

      });
    }

    openNewModal() {
      this.openModal({});
    }

    openModal(formItem) {

      var _this = this;
      var modalInstance = this.$uibModal.open({
        animation: false,
        templateUrl: '/app/products/product/productModal/productModal.html',
        controller: 'ProductModalCtrl',
        size: 'md',
        resolve: {
          formItem: function() {
            return formItem;
          }
        }
      });

      modalInstance.result.then(function(resultItem) {
        var product = resultItem;
        if (product.idProduct != null && product.idProduct > 0) {
          _this.factoryGeneral.updateProductCallback(product, function() {
            _this.getInfo();
          });
        } else {

          _this.factoryGeneral.saveProductCallback(product, function() {
            _this.getInfo();
          });
        }
      });
    }

    delete(item){
      var _this = this;
      this.$confirm({
        text: 'Are you sure you want to delete?'
      })
      .then(function() {
        _this.factoryGeneral.deleteProduct(item).then(function(info){
          _this.back();
        });
      });
    }

    back() {
      this.getInfo();
    }
  }

  angular.module('processAdminApp')
    .component('product', {
      templateUrl: 'app/products/product/product.html',
      controller: ProductComponent,
      controllerAs: '$cn'
    });

})();
